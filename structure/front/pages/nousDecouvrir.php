    <h1>A PROPOS</h1>

    <p class="text-dark">
        L'association est active de manière désintéressée, elle ne poursuit pas principalement les activités économiques,
        Cependant, Il devrait être ouvert à l'intégration et à la coopération d'autres organisations qui s'occupent des personnes handicapées; <br>
        Il s'agit aussi d'échange d'informations, d'opinions et d'expériences entre différentes cultures.
        Orientation et instruction des jeunes handicapés et non handicapés d'origine africaine à Paris dans divers instruments de musique africains.
        Cours d'Évangile pour jeunes et adultes.
        Elle contient plusieurs objectifs et s'oriente aussi pour plusieures promotions.
    </p>

    <p class="text-dark">
        Objet de l'association : cette association a pour but non-lucratif de : venir en aide aux personnes vivant avec un handicap et leurs enfants, aux orphelins et contre la discrimination ici notamment aussi en Afrique. organiser les activités culturelles telles que la musique, l'évangélisation de la parole de dieu dénommé: Zoé centre des adorateurs. à travers Zoé centre des adorateurs, l'association pourra éduquer et former des jeunes chantres (instruments musicales) et des adorateurs.
    </p>

